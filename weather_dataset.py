import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns

df = pd.read_csv('weatherHistory.csv')
print("Shape:", df.shape)
print(df.head(10))

numeric_cols = ['Temperature (C)', 'Apparent Temperature (C)',
                'Humidity', 'Wind Speed (km/h)',
                'Visibility (km)', 'Pressure (millibars)']

stats = df[numeric_cols].describe()
print(stats)

for col in numeric_cols:
    print(f"Mode of {col}: {df[col].mode()[0]:.2f}")

print("Skewness of each variable:")
for col in numeric_cols:
    print(f"{col}: {df[col].skew():.2f}")

df[numeric_cols].plot(kind='box', figsize=(14, 6),
                      title='Box Plot — Outlier Detection')
plt.tight_layout()
plt.savefig('boxplot.png')
plt.show()

df[numeric_cols].hist(figsize=(14, 8), bins=30, color='steelblue')
plt.suptitle('Histograms of Weather Variables')
plt.tight_layout()
plt.savefig('histogram.png')
plt.show()

plt.figure(figsize=(8,5))
plt.scatter(df['Temperature (C)'], df['Humidity'],
            alpha=0.3, color='teal')
plt.xlabel('Temperature (C)')
plt.ylabel('Humidity')
plt.title('Temperature vs Humidity')
plt.savefig('scatter.png')
plt.show()

plt.figure(figsize=(10,7))
sns.heatmap(df[numeric_cols].corr(), annot=True,
            cmap='coolwarm', fmt='.2f')
plt.title('Correlation Heatmap')
plt.savefig('heatmap.png')
plt.show()

